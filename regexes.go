// compile regex exp once and return it
func regexCompileOnce(s string) func() *regexp.Regexp {
	var regex *regexp.Regexp
	var once sync.Once

	return func() *regexp.Regexp {
		once.Do(func() {
			regex = regexp.MustCompile(s)
		})

		return regex
	}
}
